#!/usr/bin/env node
/**
 * Generates article registry entries from `content/articles/<collection>/<slug>/`.
 * Run before build when adding new MDX articles: `npm run sync:articles`
 */
import { existsSync, readdirSync, writeFileSync } from "node:fs"
import { join } from "node:path"

const ROOT = process.cwd()
const ARTICLES_ROOT = join(ROOT, "content/articles")
const REGISTRY_OUT = join(ROOT, "lib/content/registry.generated.ts")
const SYNC_OUT = join(ROOT, "lib/content/articles.sync.generated.ts")

function discoverArticles() {
  const entries = []

  if (!existsSync(ARTICLES_ROOT)) return entries

  for (const collection of readdirSync(ARTICLES_ROOT, { withFileTypes: true })) {
    if (!collection.isDirectory()) continue

    const collectionDir = join(ARTICLES_ROOT, collection.name)
    for (const slugDir of readdirSync(collectionDir, { withFileTypes: true })) {
      if (!slugDir.isDirectory()) continue

      const articleDir = join(collectionDir, slugDir.name)
      if (!existsSync(join(articleDir, "meta.ts"))) continue
      if (!existsSync(join(articleDir, "content.mdx"))) continue

      entries.push({
        collection: collection.name,
        slug: slugDir.name,
        key: `${collection.name}/${slugDir.name}`,
      })
    }
  }

  return entries.sort((a, b) => a.key.localeCompare(b.key))
}

function toMetaIdentifier(slug) {
  return `meta_${slug.replace(/[^a-zA-Z0-9]/g, "_")}`
}

function generateRegistry(entries) {
  const body = entries
    .map(
      (entry) => `  "${entry.key}": {
    collection: "${entry.collection}",
    slug: "${entry.slug}",
    loadMeta: () =>
      import("@/content/articles/${entry.collection}/${entry.slug}/meta").then(
        (m) => m.meta,
      ),
    loadModule: async () => {
      const [{ default: Content }, { meta }] = await Promise.all([
        import("@/content/articles/${entry.collection}/${entry.slug}/content.mdx"),
        import("@/content/articles/${entry.collection}/${entry.slug}/meta"),
      ])
      return { default: Content, meta }
    },
  },`,
    )
    .join("\n")

  return `// AUTO-GENERATED by scripts/sync-article-registry.mjs  -  do not edit manually
import type { BlogArticleMeta } from "@/lib/content/collections/blog"
import type { ResourceArticleMeta } from "@/lib/content/collections/resources"
import type { VisaGuideArticleMeta } from "@/lib/content/collections/visa-guides"
import type { ContentArticleModule } from "@/lib/content/types"

export const generatedArticleEntries = {
${body}
} as const

export type GeneratedArticleRegistryKey = keyof typeof generatedArticleEntries
`
}

function generateSyncMeta(entries) {
  const syncEntries = entries.filter((entry) => entry.collection === "blog")
  const imports = syncEntries
    .map(
      (entry) =>
        `import { meta as ${toMetaIdentifier(entry.slug)} } from "@/content/articles/${entry.collection}/${entry.slug}/meta"`,
    )
    .join("\n")

  const body = syncEntries
    .map((entry) => `  "${entry.key}": ${toMetaIdentifier(entry.slug)},`)
    .join("\n")

  return `// AUTO-GENERATED by scripts/sync-article-registry.mjs  -  do not edit manually
import type { BlogArticleMeta } from "@/lib/content/collections/blog"
import type { GeneratedArticleRegistryKey } from "@/lib/content/registry.generated"
${imports ? `\n${imports}` : ""}

export const generatedArticleMetaSync: Partial<
  Record<GeneratedArticleRegistryKey, BlogArticleMeta>
> = {
${body}
}
`
}

function main() {
  const entries = discoverArticles()
  writeFileSync(REGISTRY_OUT, generateRegistry(entries))
  writeFileSync(SYNC_OUT, generateSyncMeta(entries))
  console.log(`Synced ${entries.length} article(s) to registry.generated.ts`)
}

main()
